      <h2>Calendar</h2>
      <small>Posted on Tuesday, August 23rd, 2016</small>
      <p>
        Next feature planned is a calendar. This will be similar to the calendar
        at <a href='http://notdiychi.com' target='_blank'>not diy chi</a> in the
        collected information, but will still hold true to our values. You won't
        need to sign up or register to submit an event for listing. Basically,
        you submit your event, it sits in a queue for a little while until a
        moderator approves the event, and you're good to go, your event is up. <br /><br />
        <strong>But why wait for moderator approval?</strong><br /><br />
        I think that this feature will be heavily used and I want to make sure
        spam is thouroughly filtered. We may implement a mod approval feature
        for all listings in the future, as this site becomes more used.<br /><br />
        <strong>What info will you be collecting?</strong><br />
        <ul>
          <li>Event Name</li>
          <li>Venue Name</li>
          <li>Address</li>
          <li>DIY:MKE venue link</li>
          <li>Facebook event link</li>
          <li>Other event link (myspace? reverbnation? whatever.)</li>
          <li>Event image/flyer</li>
          <li>Cover Charge</li>
          <li>Date</li>
          <li>Start Time</li>
          <li>Type of event
            <ul>
              <li>Art Show</li>
              <li>Concert/live music</li>
              <li>Meeting (organizational, like for a festival committee or similar groups)</li>
              <li>Potluck</li>
              <li>Fundraiser/benefit show</li>
              <li>Fest</li>
              <li>Dance/party</li>
              <li>Protest/demonstration/activism</li>
            </ul>
          </li>
          <li>Age Restriction</li>
          <li>Description</li>
          <li>Host Email</li>
        </ul>
        As is normal, not all of this information is required. The only required
