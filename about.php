        <h2>How It Started</h2>
        <p>
          In about April of 2014, I (Nate Northway) was in a band with Scott Cary
          called Bastard Cat. We booked a lot of shows (both through Bastard Cat and
          through our house, Scarn Manor) and were constantly complaining that there
          wasn't a 'DIY phonebook' because booking shows through social media had
          become a nightmare of politics and people failing to respond to messages. <br />
          So Scott had this idea of starting a listing service which would serve
          to provide contact information for all sorts of people involved in Milwaukee's
          punk scene, from bands to venues to promotors and even to other related people,
          like screenprinters and tape duplicators and vinyl pressers and graphic artists and
          so on and so forth. Scott wanted a punk phonebook on the internet that was
          publically accessible. <br />
          We're not quite to Scott's original plan, but we're getting there.<br />
        </p>
