      <h2>Viewing Listings</h2>
      <p>
        Listings are viewable by visiting their group URL, listed below:<br />
        <ul>
          <li>Bands: <a href='bands.php' target='_blank'>http://diymke.org/bands</a></li>
          <li>Venues: <a href='venues.php' target='_blank'>http://diymke.org/venues</a></li>
          <li>Events: <a href='events.php' target='_blank'>http://diymke.org/events</a></li>
        </ul><br />
        <strong>View Type</strong><br /><br />
        When viewing a group of listings on desktop, the top right of the screen
        has a group of two buttons which toggles the view type. Clicking on the
        left button arranges the group items into grid and clicking the right
        button arranges the group items into a list. <br /><br />
        List is the default styling for mobile browsers and this option is not
        toggleable because the text would become hard to read.<br /><br />
        Each listing is assigned a type and unique ID number, which together,
        make it’s unique URL. To view an individual listing, click on it’s
        title in the Group View or click on the ‘Read more’ button at the bottom
        of the tile. When viewing a listing, it’s ID can be found in the URL.
        For example, our first band listing had the unique URL of
        http://diymke.org/band.php?id=1<br /><br />
        To share a listing, copy and paste the URL into an email, Facebook post,
        tweet, or any other service that is friendly to Web URLs. <br /><br />
        <strong>Editing Listings</strong><br />
        Since users do not have accounts on DIY:MKE, listings are only editable
        by moderators. The reason we do not allow accounts is to limit our
        liability and to keep everything as anonymous as possible. <br /><br />
        <strong>How To Request A Listing Edit</strong><br />
        To request for listing removal, our moderation team can be emailed at
        <a href='mailto:mods@diymke.org'>mods@diymke.org</a>. Please provide the
        listing URL and the requested changes in the email with the subject
        ‘I’d like a listing removed’.<br /><br />
        To submit a request for editing through our website, mods can also be
        contacted by filling out the contact form at
        <a href='contact' target='_blank'>http://diymke.org/contact</a>, with
        the subject ‘I’d like a listing removed’ selected. The message body must
        contain the listing URL and the reason for removal.<br /><br />
        Mods will email the original email the listing was made with to ensure
        that the request originated from the source and not a malicious user.<br /><br />
      </p>
