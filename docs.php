      <h2>General Usage</h2>
      <p>
        <strong>Types of Listings</strong><br />
        There are currently three types of listings on DIY:MKE<br /><br />
        <ul>
          <li>
            Bands
            <ul>
              <li>Active bands that are playing out</li>
            </ul>
          </li>
          <li>
            Venues
            <ul>
              <li>Active venues that have live music or host other events</li>
            </ul>
          </li>
          <li>
            Events
            <ul>
              <li>Events relevant to the punk community</li>
            </ul>
          </li>
        </ul><br />
        Listings are not curated by moderators - they are submitted to DIY:MKE
        by community members through the submission form. Bands and Venues are
        posted immediately, but events are subject to a review period (see
        Submitting Listings: Event to learn why), which can take up to 36 hours. <br /><br />
        All information gathered on the ‘submit’ form is subject to being
        displayed publically - however, users can request information be taken
        down or revised at any point by contacting moderators through the
        contact page or by emailing <a href='mailto:mods@diymke.org'>mods@diymke.org</a><br /><br />
        Listings are submitted at <a href='http://diymke.org/submit' target='_blank'>diymke.org/submit</a>,
        or by clicking ‘Submit’ in the site navigation. To submit a listing,
        select the type of listing (Band, Venue, or Event) and fill out the form
        fields. Fields that are required are marked as such. To submit the
        listing, click ‘submit’ at the bottom of the form. If the submission
        was successful, users will be shown a success message - otherwise,
        users will be shown a failure message.<br /><br />
        If the submitted listing is a band or venue, it will show up on the
        relevant pages immediately. If the submitted listing is an event, it
        will be put onto a moderation list and posted once a moderator has
        approved the event.<br /><br />
        <strong>Submitting Listings: Bands</strong><br />
        To submit a listing for a band, the following information is requested:
        the band’s name, location (neighborhood-level), contact email, Facebook
        URL, Link to music (Bandcamp preferred), other website URL, genres,
        description, and year of establishment. The only required information is
        the band’s name and contact email. It is strongly suggested to include
        all information requested to give the best idea of the band. <br /><br />
        Band Listings appear immediately on <a href='http://diymke.org/bands' target='_blank'>diymke.org/bands</a><br /><br />
        <strong>Submitting Listings: Venues</strong><br />
        To submit a listing for a venue, the following information is requested:
        the venue’s name, location (neighborhood-level or exact address),
        contact email, a description, associated genres, the year established,
        Facebook URL, and website URL. The only required information is the
        venue name and contact email, but it is strongly suggested to provide
        all information where possible to provide the best picture of the venue.<br /><br />
        Venue listings appear immediately on <a href='http://diymke.org/venues' target='_blank'>diymke.org/venues</a><br /><br />
        <strong>Submitting Listings: Event</strong><br />
        To submit an event listing, the following information is requested:
        Event name, the venue, the venue address, the DIY:MKE venue listing link,
        Facebook event link, other event link (such as BandsInTown or similar),
        the cover charge, date, start time, type of event, the age restriction,
        event description, an image to represent the event (such as a flyer),
        and host email address. It is strongly suggested to provide as much
        information as possible. Event name, venue, type, age restriction, date,
        start time, and host email are all required. <br /><br />
        Event listings do not appear publically until a moderator approves the
        listing. The purpose for moderator review is to limit liability to
        DIY:MKE. Since user postings are anonymous, all content on the site is
        the sole responsibility of DIY:MKE. To limit our liability, we must
        review images that could be posted on the site for copyright
        infringement. <br /><br />
        When a listing is approved or denied, the host email is sent an alert
        which contains the status of their event and the link (if approved) or
        what further action can be taken (if denied).<br /><br />
        The host email will never be published. It is kept secure and used for
        communication purposes between DIY:MKE moderators and the event host.<br /><br />
