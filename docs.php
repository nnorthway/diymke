      <h2>DIY:MKE Introduction</h2>
      <p>
        <strong>Who We Are</strong><br />
        Hi, I’m Nate Northway, I helped bring DIY:MKE to it’s feet through
        building, maintaining, testing, and actively developing the site. I’m
        going to tell our story and guide you through the use of the DIY:MKE
        App. First, I’m going to introduce myself.<br /><br />
        I’ve been a part of Milwaukee’s punk scene for about 5 years now. I’ve
        been playing in bands that whole time, I’ve booked a lot of shows, and
        I’ve organized a few tours. For the last 2 years, I’ve been doing web
        development. I am constantly working on something that I want to use to
        help people. I’ve built a blogging system, lots of tutorials and element
        samples, and of course, DIY:MKE.<br /><br />
        Also on the development team are the beta testers. I could not have
        rolled out features quickly without Jason LeGear, Alex Bischmann, JJ Kaiser,
        and Hal Crossno. They helped me test features and edge cases to ensure
        not only that everything worked as it should but that everything looked
        consistent across platforms. They also made listings and actions easier
        to understand through providing feedback and offering suggestions, all of
        which I am forever greatful. So thank you, beta team. <br /><br />
        <strong>Why DIY:MKE Was Built</strong><br />
        I built DIY:MKE to help curb a common issue in Milwaukee’s punk/DIY
        scene: constant Facebook posts of ‘Who wants to jump on this’ but nobody
        responding. The reasons for non-response are numerous. DIY:MKE allows
        promoters to say ‘Can YOU hop on this show?’ to target their questions.
        It helps promoters know what is out there. <br /><br />
        It also helps bands play out more. A band that is lesser known may have
        an easier time getting on shows with DIY:MKE. It also helps venues get
        more exposure. A spot that is out-of-the-way or relatively unknown may
        become more popular because people book there more.<br /><br />
        And now, with events, it helps people stay in the loop more. A lot of
        people don’t have social media accounts or are working/doing their own
        thing a lot and miss out on a lot of great shows. DIY:MKE is another
        promotional outlet, not to replace, but to add to the powers of social
        media. <br /><br />
        <strong>App Purpose</strong><br />
        So, in essence, DIY:MKE exists to better the experiences of bands,
        promoters, and venues in the Milwaukee scene, where they otherwise may
        not be as intricately connected. DIY:MKE also exists to aid the booking
        and promotion of punk events, including, but not limited to, activism,
        workshops, festivals, and other punk happenings.<br /><br />
      </p>
      <br />
      <h3>Our Mission Statement</h3><br />
      <p class='lead'>
        DIY:MKE provides promotional tools for Milwaukee’s Punk/DIY Community
        that better connect members through an anonymous platform that protects
        the identities of users and aligns with the underground culture
        synonymous with punk.
      </p>
